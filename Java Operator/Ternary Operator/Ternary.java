/* Ternary operator */

class Ternary
{
	public static void main(String[] args)
	{
		int a=10,b=20,Max;
		Max=(a>b) ? a:b;
		System.out.println(Max);
	}
}

/*  -->In this case if condition (a>b) is true then print a
       if condition is false then print b
	-->In general if condition is true then print :(colon) left side part
	   if condition is false then print :(colon) right side part
*/	   