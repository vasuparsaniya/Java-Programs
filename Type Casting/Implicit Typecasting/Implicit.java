/* Implicit Type casting */

class Implicit
{
	public static void main(String[] args)
	{
		int a=10;    //int size 4 bytes
		double b=a; //double 8 bytes
		
		System.out.println(b);                     
	}
}

/*  -->In implicit type casting means   small size data type->Big size data type
    -->convert int to double
*/

