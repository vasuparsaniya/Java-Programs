/* with out useing foreach loop print all element of Array */

class A
{
	public static void main(String[] args)
	{
		int a[]={10,20,30,40,50};
		
		for(int i=0;i<a.length;i++)
		{
			System.out.println(a[i]+" ");
		}
	}
}

/*  -->In this case with out using for each loop print all element of Array
    -->a.length means length of array
	-->i is index of array element
*/